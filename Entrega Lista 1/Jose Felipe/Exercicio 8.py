# 8. Crie um programa em Python para converter e exibir uma temperatura de graus Celsius para 
# graus Fahrenheit.
# 𝐹 =
# 9 ∗ 𝐶 + 150

Celsius = float(input("Informe a temperatura em graus celsius: "))
Fahrenheit = (9*Celsius+150)/5

print(f'A temperatura em graus Fahrenheit é de: {Fahrenheit} graus')


