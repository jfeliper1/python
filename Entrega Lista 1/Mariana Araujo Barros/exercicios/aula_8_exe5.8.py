# - Raiz quadrada dos dois números [math.sqrt()]

import math
num1 = float(input("Digite o primeiro valor: "))
num2 = float(input("Digite o segundo valor: "))

raiz_num1 = [math.sqrt(num1)]

raiz_num2 = [math.sqrt(num2)]

print(f"A raiz do primeiro número é :{raiz_num1} \n A raiz do segundo número é: {raiz_num2}")
