""". Crie um programa em Python que solicite o valor da base e altura e calcule a área de um 
retângulo, apresentando no final todos os valores na tela.
area_retangulo = base x altura
"""

base = float(input('\nDigite o valor da base: \n'))
altura = float(input('\nDigite o valor da altura: \n'))

area = base * altura

print(f'\nA area do retangulo de acordo com a BASE: {base}cm e a ALTURA: {altura}cm é: {area}cm\n')