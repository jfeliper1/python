n1 = float(input("Digite um número "))
n2 = float(input("Digite outro número "))

import math

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
potencia = n1 ** n2
raizN1 = math.sqrt(n1)
raizN2 = math.sqrt(n2)


print(f"A soma dos dois números é {soma}")
print(f"A subtração dos dois números é {subtracao}")
print(f"A multiplicação dos dois numeros é {multiplicacao}")
print(f"A divisao dos dois numeros é {divisao}")
print(f"A potencia do primeiro numero elevado pelo segundo numero é {potencia}")
print(f"A raiz quadrada do primeiro numero é {raizN1}")
print(f"A raiz quadrada do segundo numero é {raizN2}")
